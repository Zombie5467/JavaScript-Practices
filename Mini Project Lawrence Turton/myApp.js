let elements7 = document.getElementsByName('cssProperty');
let div7 = document.getElementById('modify');

function set() {
    for (let index7 = 0; index7 < elements7.length; index7++) {
        
        let property7 = elements7[index7].getAttribute('id');
        let value7 = elements7[index7].value;
        div7.style[property7] = value7;
    }
}

document.getElementById('set').addEventListener('click', set);




